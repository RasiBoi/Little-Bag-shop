private void loadBagData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        ArrayList<Bag> bags = bagManager.loadBags();
        for (Bag bag : bags) {
            model.addRow(new Object[]{bag.getBagID(), bag.getBagBrand(), bag.getCategory(), bag.getBagPrice()});
        }
    }

    
    private void DeletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletebtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String bagID = model.getValueAt(selectedRow, 0).toString();
        model.removeRow(selectedRow);
        bagManager.deleteBag(bagID);
        JOptionPane.showMessageDialog(this, "Bag deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_DeletebtnActionPerformed

    private void UpdatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatebtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String bagID = model.getValueAt(selectedRow, 0).toString();
        String bagBrand = model.getValueAt(selectedRow, 1).toString();
        String category = model.getValueAt(selectedRow, 2).toString();
        String bagPrice = model.getValueAt(selectedRow, 3).toString();

        Bag updatedBag = new Bag(bagID, bagBrand, category, bagPrice);
        bagManager.updateBag(updatedBag);
        JOptionPane.showMessageDialog(this, "Bag updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_UpdatebtnActionPerformed

