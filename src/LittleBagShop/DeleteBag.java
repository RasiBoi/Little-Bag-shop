    private void updateBagsFile() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    ArrayList<String> bagData = new ArrayList<>();

    for (int i = 0; i < model.getRowCount(); i++) {
        String bagID = model.getValueAt(i, 0).toString();
        String supplierID = model.getValueAt(i, 1).toString();
        String category = model.getValueAt(i, 2).toString();
        String bagPrice = model.getValueAt(i, 3).toString();
        bagData.add(bagID + "," + supplierID + "," + category + "," + bagPrice);
    }

    try (BufferedWriter bw = new BufferedWriter(new FileWriter("bags.txt"))) {
        for (String line : bagData) {
            bw.write(line);
            bw.newLine();
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}
    
    
    
